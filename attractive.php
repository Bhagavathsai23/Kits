								  <td colspan="5"><blockquote> 
									  <!--<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Attractive features desirable to employers</strong><br></p>-->
										
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Confidence</strong><br></p>
<p align="left"><font size="3" face="Century Gothic" color="black" >
This can be expressed in so many ways. Look the interviewer in the eye, shake their hand firmly. Be chatty and lucid, sit with a good, straight posturewith your hands on you lap. Be precise with your answers, do not say 'um'  or 'ah'. Confidence comes with experience, so tell the interviewer about different situations that you negotiated in your previous jobs, perhaps even mention your world travels or a time you held a position of power with an organisation.
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Approachability</strong><br></p>
<p align="left"><font size="3" face="Century Gothic" color="black" >
To express approachability be friendly and open, smile and pleasantly greet everyoneyou are introduced to. Perhaps you trained other personnel in a previous job, or you were a counsellor at college. Recount an occasion when professionally someone came to with there problems.
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Sense of humour</strong><br></p>
<p align="left"><font size="3" face="Century Gothic" color="black" >
Dont set about cracking jokes, but be responsive if the interviewer makes a humorous comment (even if it is not very funny). If you can subtly incorporate humour which is not at the expense of anyone else then do so, but bear in mind that this is a  business interview and you should be professional.
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Responsibility/trustworthiness</strong><br></p>
<p align="left"><font size="3" face="Century Gothic" color="black" >
These qualities cannot be really seen in a person, but you can recount the times in your previous jobs that you have held positions of responsibility. For example any job in which you had personnel beneath you, or if you were a scout/Guide leader.  
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Initiative/leadership</strong><br></p>
<p align="left"><font size="3" face="Century Gothic" color="black" >
These qualities are linked to confidence, but in addition show the employer examples of times you have used these skills and have prospered as a result. For example, you worked on a software solution to a work problem in your own time and it turned out to increase the efficiency of the task by 60%. 
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Body language</strong><br></p>
<p align="left"><font size="3" face="Century Gothic" color="black" >
A first impression can be greatly influenced by a persons body language.  You are in trouble if all the right things are coming from your mouth, but your body is doing all the wrong things. 
</font></p>
