								  <td colspan="5"> <h1 align="center"><font color="black" face="Century Gothic" size="5">Typical Interview Questions and Best Responses</font></h1></td>
								</tr>
								<tr> 
								  <td colspan="5"><blockquote> 
									  <p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Tell me about yourself</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
							<ul>
							<li>Keep your answer to one or two minutes; don't ramble. </li>
<li>Do not go back to childhood experiences, just give a brief outline of where you are from, and where you want to be going.</li> 
<li>Use your CV introduction as a base to start. </li>
<li>Respond in a way that leaves no doubt that you are well adjusted, stable and positive.</li> 
<li>Say only positive statements</li>
</ul>
</font></p> 
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>What do you know about our company?</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
Show that you have done your research, know what their products are, how big the company is, roughly what their annual revenue is, what reputation it has within the industry and on the street. Know the company's history, image, goal, and philosophy. </li>
<li>Project an informed interest and let the interviewer tell you some more detailed aspects about the company.
</li>
</ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Why do you want to work for us?</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" > 
<ul><li>
Don't talk about what you want; first talk about their needs, what you can do for them. </li>
<li>You wish to be part of their company project. </li>
<li>You would like to solve their company problem and relish the challenge. </li>
<li>You can make a definite contribution to specific company goals: identify its management talent, etc.
</li>
</ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>What would you do for us? What can you do for us that someone else can't? </strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" > 
<ul><li>
Relate past experiences that represent success in solving previous employer problems that may be similar to those of the prospective employer. </li>
<li>Stay positive 
</li></ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>What about our position do you find the most attractive? Least attractive? </strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
List three or more attractive factors and only one minor unattractive factor, aspects to pick up on could include, office location, company reputation, the chance to work with esteemed colleagues. 
</li></ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Why should we hire you?</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
Because of the knowledge, experience, abilities, and skills you possess. Be very positive and confident in your reply, not vague. 
</li></ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>What do you look for in a job?</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
An opportunity to use my skills, to perform and be recognised. </li>
<li>The opportunity to develop further skills, throughout life we should be constantly learning. </li>
<li>Vague answers such as, "I enjoy working with people, I relish challenges in my work" </li>
<li>Relate it to the job to which you are applying "I am especially interested in producing a software solution to your problem" </li>
</li></ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>Please give me your definition of a ... (the position for which you are being interviewed). </strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
Keep it brief, actions and results oriented 
</li></ul>
</font></p>
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>How long would it take you to make a meaningful contribution to our firm?</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
Very quickly after a little orientation and a brief period of adjustment on the learning curve.
</li></ul>
</font></p> 
<p align="justify"><font size="3" font color="black" face="Century Gothic" size="3"><strong>How long would you stay with us?</strong><br></p>
							<p align="left"><font size="3" face="Century Gothic" color="black" >
<ul><li>
As long as we both feel I'm contributing, achieving, growing etc.
</li></ul>
							</font></p>
